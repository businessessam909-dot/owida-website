document.addEventListener('DOMContentLoaded', () => {
    // Initialize Theme
    const savedTheme = localStorage.getItem('owida_theme') || 'light';
    document.body.setAttribute('data-theme', savedTheme);
    updateThemeIcon(savedTheme);

    // Initialize Language
    const savedLang = localStorage.getItem('owida_lang') || 'en';
    setLanguage(savedLang);

    // Event Listeners
    document.getElementById('btn-theme-toggle').addEventListener('click', toggleTheme);
    document.getElementById('btn-lang-en').addEventListener('click', () => setLanguage('en'));
    document.getElementById('btn-lang-ar').addEventListener('click', () => setLanguage('ar'));

    // Simple Interactive Parallax
    document.addEventListener('mousemove', (e) => {
        const moveX = (e.clientX * 0.01);
        const moveY = (e.clientY * 0.01);

        document.querySelectorAll('.trust-item, .review-card, .feature-card, .app-icon-link').forEach(el => {
            el.style.transform = `translate(${moveX}px, ${moveY}px)`;
        });
    });
});
